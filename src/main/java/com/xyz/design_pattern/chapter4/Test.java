    // 面对需求，对程序的改动是通过增加新代码进行的，而不是更改现有的代码，是开放封闭原则的精神所在

    // 面向对象的核心所在，可维护，可扩展，可复用，灵活性好
    // 开发人员应该近对程序中呈现出频繁变化的那些播放做出抽象，然而，对应应用程序中的每个部分都刻意进行抽象同样不是一个好主意
    // 拒绝不成熟的抽象和抽象本身一样重要

